exports.main = async (event) => {
  const db = cloud.database();
  const today = new Date();
  const dateKey = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;

  // 用日期字符串哈希稳定选一条
  let hash = 0;
  for (let i = 0; i < dateKey.length; i++) {
    hash = ((hash << 5) - hash) + dateKey.charCodeAt(i);
    hash |= 0;
  }
  const idx = Math.abs(hash) % TIPS.length;
  const tip = TIPS[idx];

  // 写一条记录 (运营可看活跃度)
  try {
    await db.collection("daily_tips_logs").add({
      data: {
        date_key: dateKey,
        tip_index: idx,
        tip_text: tip,
        created_at: Date.now(),
      },
    });
  } catch (e) {
    // 不要因为日志失败影响 tip 返回
  }

  return {
    code: 0,
    date_key: dateKey,
    tip: tip,
    idx: idx,
  };
};
